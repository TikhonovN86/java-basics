package org.itmo.lesson2;

public class Calculator {
    static double calculateSum(double z, double x) {
        return z + x;
    }

    static long calculateSum(long z, long x) {
        return z + x;
    }

    static int calculateSum(int z, int x) {
        return z + x;
    }

    static double calculateDifference(double z, double x) {
        return z - x;
    }

    static long calculateDifference(long z, long x) {
        return z - x;
    }

    static int calculateDifference(int z, int x) {
        return z - x;
    }

    static double calculateProduct(double z, double x) {
        return z * x;
    }

    static long calculateProduct(long z, long x) {
        return z * x;
    }

    static int calculateProduct(int z, int x) {
        return z * x;
    }

    static double calculateQuotient(double z, double x) {
        return z / x;
    }

    static long calculateQuotient(long z, long x) {
        return z / x;
    }

    static int calculateQuotient(int z, int x) {
        return z / x;
    }

}
